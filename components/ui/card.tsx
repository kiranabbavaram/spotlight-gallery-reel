import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { 
    hover?: boolean; 
    gradient?: boolean;
  }
>(({ className, hover = false, gradient = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border bg-card text-card-foreground relative overflow-hidden",
      hover && "transition-all duration-300 hover:shadow-hover-card hover:-translate-y-0.5",
      gradient && "before:absolute before:inset-0 before:-z-10 before:bg-gradient-subtle before:opacity-25 before:rounded-xl",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

